package dat.nguyenxuan.apponkiemtragiuaki;

public class FoodItem {
    private String name;
    private String image;

    public FoodItem(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }
}
